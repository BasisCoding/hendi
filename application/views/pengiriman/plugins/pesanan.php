
<script type="text/javascript">

	var table;
	$(document).ready(function() {

		table = $('#table-pesanan').DataTable({
			"processing": true, 
            "serverSide": true,
            // "scrollX": true,
            // "fixedColumns": {
            // 	 "leftColumns": 1,
            // 	 "rightColumns": 2
            // },
            // "responsive": true,
            // "lengthChange": false,
            "order": [],
            "autoWidth" : true,
             
            "ajax": {
                "url": "<?= base_url('pengiriman/Pesanan/get_pesanan')?>",
                "type": "POST"
            },

            "language": {
		        "paginate": {
		            "previous": '<i class="fas fa-angle-left"></i>',
      				"next": '<i class="fas fa-angle-right"></i>'
		        },
		        "aria": {
		            "paginate": {
		                "previous": 'Previous',
		                "next":     'Next'
		            }
		        }
		    },
		});

		function reload_table() {
			table.ajax.reload(null, false);
		}


		function actionData(formData, action) {
			$.ajax({
				url: '<?= base_url("pengiriman/Pesanan/") ?>'+action+'',
				type: 'POST',
				dataType: 'JSON',
				data: formData,
				processData: false,
		        contentType: false,
				success:function (response) {
					$.notify({
	                    icon: 'ni ni-bell-55',
	                    message:response.message
	                },{
	                    type:response.type,
	                    z_index:2000,
	                    placement: {
	                      from: "top",
	                      align: "right"
	                	},
	                    animate: {
	                      enter: 'animated fadeInDown',
	                      exit: 'animated fadeOutUp'
	                 	}
	                });

					if (response.type == 'success') {
						$('#modal-'+action).modal('hide');
	            		$('#form-'+action)[0].reset();
					}
				}
			});
		}

		$('#form-KirimPesanan').submit(function() {
			var formData = new FormData();
            formData.append('id', $('[name="id_pesanan_kirim"]').val()); 
			
			actionData(formData, 'KirimPesanan');
        	reload_table();

        	return false;
		});

		$('#table-data-pesanan').on('click', '.kirim-pesanan', function() {
			var id = $(this).attr('data-id');
			var kode = $(this).attr('data-kode');
			
			$('#pesanan-kirim').html(kode);
			$('[name="id_pesanan_kirim"]').val(id);

			$('#modal-KirimPesanan').modal('show');
		});

	});
</script>
